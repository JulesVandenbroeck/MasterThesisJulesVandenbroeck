def Binning(leading_lepton_pt,nJets,nBJets):
    BinNr = 0

    if nJets == 4:
        BinNr +=1
    elif nJets >= 5:
        BinNr +=2

    if nBJets >= 2:
        BinNr += 3

    if leading_lepton_pt >= 100:
        BinNr += 6

    return BinNr
